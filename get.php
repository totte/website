        <li>Chakra ISO images do not support <strong>unetbootin</strong> and DVDs need to be burned at a speed <strong>no higher than 4Ã—</strong>. To create reliable installation media, please follow the instructions on our <a href="http://chakra-project.org/wiki/index.php?title=Bootable_Medium_Creation" title="Bootable Chakra media  ">wiki instructions</a>.
